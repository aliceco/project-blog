        Information om insamling av personuppgifter
        Vilka uppgifter insamlas/lagras?
        Ditt användarnamn plus all annan information som du väljer att ladda upp till webbservern.

        För vilka ändamål kommer uppgifterna att behandlas? Och vem samlar in dem?
        Skapandet av den här webbplatsen ingår som en del av kursuppgifter för studenter vid Luleå tekniska universitet
        i kursen Webbutveckling 2 där syftet är att studenterna ska lära sig koda skriptspråk och databaser. Om du
        väljer att skapa en användare kommer Luleå tekniska universitet att samla in ditt valda användarnamn för att
        kunna administrera ditt konto samt eventuella andra uppgifter som du väljer att uppge. Uppgifterna sparas i
        webbservern som tillhandhålls av Microsoft Azure.

        Vad är den rättsliga grunden för insamling?
        Samtycke är den rättsliga grunden. Det innebär att ditt samtycke krävs för att uppgifterna ska kunna samlas in.
        Du kan när som helst ta tillbaka ditt samtycke och dina uppgifter kommer då att tas bort.

        Hur länge uppgifter kommer lagras?
        Uppgifterna kommer att lagras till kursen är betygssatt, om inte du dessförinnan har bett oss att ta bort dem.

        Vilka vi delar uppgifterna vidare till?
        Tillhandahållaren av plattformen kan ta del av uppgifterna vid support. Tillhandahållaren lagrar uppgifterna i
        Norra Europa.

        Kommer att överföras till ett land utanför EU/EES?
        Nej.

        Tänk på!
        Dela inga uppgifter om dig själv eller andra som inte får spridas eller som du anser är känsliga. Det finns
        alltid en risk för läckage.

        Kontaktuppgifter
        Vid frågor kan du kontakta kursansvarig Susanne Fahlman, susanne.fahlman@ltu.se.

        Om du är missnöjd med universitetets behandling av dina personuppgifter kan du vända dig till
        Integritetsskyddsmyndigheten. Du kan också vända dig till universitetets dataskyddsombud via dataskydd@ltu.se.

        Läs mer om universitets behandling och dina rättigheter enligt GDPR här:

        Behandling av personuppgifter, GDPR | Luleå tekniska universitet (ltu.se)
